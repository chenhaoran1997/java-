public class OperatorDemo1 {
    public static void main(String[] args) {
        int a=5;
        int b=2;
        System.out.println(a%b);
        System.out.println(a/b);
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println("-------------------------------");
        System.out.println(5/4);
        System.out.println(5.0/4);
        System.out.println(5/4.0);
    }
}
